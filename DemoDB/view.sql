DROP TABLE IF EXISTS Llibres;
DROP TABLE IF EXISTS Clients;

-- =========================
-- 1) Tablas
-- =========================

CREATE TABLE Clients (
  id_client INT NOT NULL PRIMARY KEY,
  nom VARCHAR(50) NOT NULL,
  cognom VARCHAR(50) NOT NULL,
  email VARCHAR(100) NOT NULL,
  provincia VARCHAR(50) NOT NULL,
  poblacio VARCHAR(100) NOT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_spanish_ci;

CREATE TABLE Llibres (
  id_llibre INT NOT NULL PRIMARY KEY,
  titol VARCHAR(150) NOT NULL,
  autor VARCHAR(100) NOT NULL,
  tema VARCHAR(50) NOT NULL,
  any_publicacio INT NOT NULL,
  preu DECIMAL(10,2) NOT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_spanish_ci;

CREATE TABLE Vendes (
  id_venda INT NOT NULL PRIMARY KEY,
  id_client INT NOT NULL,
  data_venda DATE NOT NULL,
  CONSTRAINT fk_vendes_client FOREIGN KEY (id_client) REFERENCES Clients(id_client)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_spanish_ci;

CREATE TABLE Detall_Vendes (
  id_venda INT NOT NULL,
  id_llibre INT NOT NULL,
  quantitat INT NOT NULL,
  preu_unitari DECIMAL(10,2) NOT NULL,
  PRIMARY KEY (id_venda, id_llibre),
  CONSTRAINT fk_detall_vendes_venda FOREIGN KEY (id_venda) REFERENCES Vendes(id_venda),
  CONSTRAINT fk_detall_vendes_llibre FOREIGN KEY (id_llibre) REFERENCES Llibres(id_llibre)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_spanish_ci;

